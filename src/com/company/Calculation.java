package com.company;

public class Calculation {

    public int addition(int a, int b){
        int sum = a + b;
        return sum;
    }

    public int subtraction(int a, int b) {
        int difference = a - b;
        return difference;
    }
    public int multiplication(int a, int b) {
        int mult = a * b;
        return mult;
    }

    public int division(int a, int b) {
        int div = a / b;
        return div;
    }
}
